namespace WeaponSystem.Interface
{
    /// <summary>
    /// ダメージを受けることができるインターフェース
    /// </summary>
    public interface IDamageable
    {
        // ======================================================
        // パブリックメソッド
        // ======================================================

        /// <summary>
        /// ダメージを受ける処理
        /// 実装側で HP 減算やエフェクト発火などを行う
        /// </summary>
        /// <param name="damage">受けるダメージ量</param>
        void TakeDamage(float damage);
    }
}
