namespace WeaponSystem.Data
{
    /// <summary>
    /// 弾丸タイプを識別するための列挙体
    /// 各弾丸は挙動が異なるため、BulletFactory で
    /// 適切な BulletBase の派生クラスを選択するために使用される
    /// </summary>
    public enum BulletType
    {
        // --------------------------------------------------
        // 榴弾（Explosive）
        // --------------------------------------------------
        // 着弾した地点で爆発を発生させる弾丸タイプ
        // 爆発半径や爆発ダメージを持つ
        Explosive,

        // --------------------------------------------------
        // 徹甲弾（Penetration）
        // --------------------------------------------------
        // 複数の対象を貫通可能な弾丸タイプ
        // 貫通可能速度を持つ
        Penetration,

        // --------------------------------------------------
        // 誘導弾（Homing）
        // --------------------------------------------------
        // 敵の位置を追跡しながら飛行する弾丸タイプ
        // 方向補正や旋回速度を持つ
        Homing
    }
}