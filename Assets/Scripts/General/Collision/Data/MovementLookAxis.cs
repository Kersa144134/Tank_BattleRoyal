using System;

namespace CollisionSystem.Data
{
    // ======================================================
    // 列挙体
    // ======================================================

    /// <summary>
    /// 移動制限される軸方向
    /// </summary>
    [Flags]
    public enum MovementLockAxis
    {
        /// <summary>制限なし</summary>
        None = 0,

        /// <summary>X 軸移動禁止</summary>
        X = 1 << 0,

        /// <summary>Z 軸移動禁止</summary>
        Z = 1 << 1,

        /// <summary>全軸移動禁止</summary>
        All = X | Z
    }
}